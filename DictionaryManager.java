    public String translate(String text, String sourceLang, String targetLang) {
        String dictKey = sourceLang + "_" + targetLang;
        Map<String, String> dictionary = dictionaries.get(dictKey);
        
        if (dictionary == null) {
            return "[Словарь не найден для " + sourceLang + " -> " + targetLang + "]";
        }
        
        StringBuilder result = new StringBuilder();
        StringBuilder currentWord = new StringBuilder();
        
        for (int i = 0; i < text.length(); i++) {
            char c = text.charAt(i);
            
            if (Character.isLetter(c)) {
                currentWord.append(c);
            } else {
                if (currentWord.length() > 0) {
                    result.append(translateWord(currentWord.toString(), dictionary));
                    currentWord.setLength(0);
                }
                result.append(c);
            }
        }
        
        if (currentWord.length() > 0) {
            result.append(translateWord(currentWord.toString(), dictionary));
        }
        
        return result.toString();
    }
    
    private String translateWord(String word, Map<String, String> dictionary) {
        String lowerWord = word.toLowerCase();
        String translation = dictionary.get(lowerWord);
        
        if (translation == null) {
            return "[" + word + "]";
        }
        
        if (Character.isUpperCase(word.charAt(0)) && translation.length() > 0) {
            translation = Character.toUpperCase(translation.charAt(0)) + 
                         translation.substring(1);
        }
        
        return translation;
    }
    
    public void addWord(String word, String translation, String sourceLang, String targetLang) {
        String dictKey = sourceLang + "_" + targetLang;
        Map<String, String> dictionary = dictionaries.get(dictKey);
        
        if (dictionary != null) {
            dictionary.put(word.toLowerCase(), translation.toLowerCase());
        }
    }
    
    public int getDictionarySize(String sourceLang, String targetLang) {
        String dictKey = sourceLang + "_" + targetLang;
        Map<String, String> dictionary = dictionaries.get(dictKey);
        return dictionary != null ? dictionary.size() : 0;
    }
